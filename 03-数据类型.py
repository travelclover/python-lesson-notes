# 数据类型
# 数值 int整形 float浮点型
# 布尔型 True真 False假
# str字符串
# list字符串
# tuple元组
# set集合
# dict字典

num1 = 1 # int 整形
num2 = 1.1 # float 浮点型
print(type(num1))
print(type(num2))

# str
str1 = 'hello world'
print(type(str1))

# bool
boolean = True
print(type(boolean))

# list
list1 = [1, 2, 3, 4]
print(type(list1))

# tuple
tuple1 = (1, 2, 3)
print(type(tuple1))

# set
set1 = {10, 20, 30}
print(type(set1))

# dict
dict1 = {
  'a': 'a',
  'b': 'b',
}
print(type(dict1))